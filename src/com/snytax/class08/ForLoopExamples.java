package com.snytax.class08;

public class ForLoopExamples {

	public static void main(String[] args) {
		
		// I need to print number from 1 to 90
		
		for(int i =1; i<=90; i++) {
			
			System.out.print(i+" ");
			
		}
		// I need number from 60 to 10
		
		for(int i =60; i>=10; i--) {
			
			
			System.out.print(i+" ");
		}

	}

}
